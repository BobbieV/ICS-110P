# print() function with end=

print('Hello', end='')
print('Goodbye!', end=" ")
print("What's up?", end="@@@@@@")
print("Nothing")

# Print a variable value
my_age = 100
print("My age is:", end=' ')
print(my_age)

# Print multiple items w/1 print() statement
intro = "Hi! My name is:"
name = 'Nikki'
print(intro, name)

# Print my favorite animals
print("Here are my favorite animals:")
print("1. Aardvarks\n2. Ostrich")
print("3. Honey Badger")
print()


# Example of \n
print("1\n2\n3\nGo!")

# Example of \' and \"
